class Relationship(BaseModel):
    """
    A discovered relationship between two columns in the dataset.
    Produced by the LangGraph agent in Stage 3 (reason_step).

    The LLM (Qwen 2.5 72B) validates and filters candidate relationships,
    assigning confidence scores and suggesting visualization types.

    Attributes:
        source_column: The source/reference column
        target_column: The target/dependent column
        relationship_type: Type of relationship between the columns
        confidence: AI confidence score [0.0, 1.0] — MUST be >= 0.65 to pass gate
        description: Human-readable description of the relationship
        chart_hint: Recommended chart type for visualizing this relationship
        correlation_coefficient: Statistical correlation if computed
        analytical_purpose: Business value of understanding this relationship
    """
    source_column: str = Field(..., description="Source/reference column name")
    target_column: str = Field(..., description="Target/dependent column name")
    relationship_type: RelationshipType = Field(
        ..., description="Type of relationship"
    )
    confidence: float = Field(
        ..., ge=0.0, le=1.0, description="AI confidence score (must be >= 0.65)"
    )
    description: str = Field(
        ..., min_length=1, description="Human-readable description"
    )
    chart_hint: ChartType = Field(
        ..., description="Recommended chart type for visualization"
    )
    correlation_coefficient: Optional[float] = Field(
        None, description="Statistical correlation if computed"
    )
    analytical_purpose: Optional[str] = Field(
        None, description="Business value of understanding this relationship"
    )

    @field_validator("confidence")
    @classmethod
    def validate_confidence_gate(cls, v: float) -> float:
        """Warn if confidence is below the gate threshold."""
        if v < 0.65:
            import warnings
            warnings.warn(
                f"Relationship confidence {v:.2f} is below gate threshold of 0.65. "
                "This relationship may be filtered out by the validation gate."
            )
        return max(0.0, min(1.0, v))


