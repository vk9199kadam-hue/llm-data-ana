class ColumnInference(BaseModel):
    """
    Schema inference result for a single column from a CSV file.
    Produced by Qwen 2.5 72B in Stage 1.

    Attributes:
        column_name: The original column header from the CSV
        detected_type: Inferred data type for the column
        format_spec: Optional format specification (e.g., date format "%Y-%m-%d")
        confidence: Confidence score [0.0, 1.0] for the type inference
        reasoning: LLM explanation for why this type was chosen
        nullable: Whether the column contains null values
        sample_values: Up to 5 sample values from the CSV for verification
    """
    column_name: str = Field(..., description="Original column header from CSV")
    detected_type: DataType = Field(..., description="Inferred data type")
    format_spec: Optional[str] = Field(None, description="Format spec (e.g., date format)")
    confidence: float = Field(
        ..., ge=0.0, le=1.0, description="Confidence score [0.0, 1.0]"
    )
    reasoning: str = Field(..., min_length=1, description="LLM explanation for inference")
    nullable: bool = Field(True, description="Whether column contains nulls")
    sample_values: List[Optional[str]] = Field(
        default_factory=list, max_length=5, description="Up to 5 sample values"
    )

    @field_validator("confidence")
    @classmethod
    def clamp_confidence(cls, v: float) -> float:
        """Clamp confidence to valid range."""
        return max(0.0, min(1.0, v))

    @field_validator("column_name")
    @classmethod
    def validate_column_name(cls, v: str) -> str:
        """Ensure column name is not empty and has reasonable length."""
        if not v or len(v.strip()) == 0:
            raise ValueError("Column name cannot be empty")
        if len(v) > 255:
            raise ValueError("Column name exceeds maximum length (255)")
        return v.strip()


class SchemaInferenceResponse(BaseModel):
    """
    Complete schema inference result for a CSV file.
    Output of Stage 1 — passed as input to Stage 2 (Data Cleaning).

    Attributes:
        columns: List of inferred column schemas
        row_count: Number of rows detected in the CSV
        encoding: Detected file encoding (via chardet)
        detected_delimiter: Detected CSV delimiter character
        has_header: Whether the CSV has a header row
        overall_confidence: Average confidence across all columns
        file_hash: MD5 hash of the original file for caching
        processing_time_ms: Time taken for schema inference
    """
    columns: List[ColumnInference] = Field(
        ..., min_length=1, description="All inferred column schemas"
    )
    row_count: int = Field(..., ge=0, description="Number of rows in CSV")
    encoding: str = Field(default="utf-8", description="Detected file encoding")
    detected_delimiter: str = Field(default=",", description="CSV delimiter character")
    has_header: bool = Field(True, description="Whether CSV has a header row")
    overall_confidence: Optional[float] = Field(
        None, ge=0.0, le=1.0,
        description="Average confidence across all columns (auto-computed if not provided)"
    )
    file_hash: Optional[str] = Field(None, description="MD5 hash for caching")
    processing_time_ms: Optional[int] = Field(None, description="Processing time")

    @model_validator(mode="after")
    def compute_overall_confidence(self) -> "SchemaInferenceResponse":
        """Auto-compute overall confidence from individual column confidences."""
        if self.columns and self.overall_confidence is None:
            self.overall_confidence = round(
                sum(c.confidence for c in self.columns) / len(self.columns),
                4,
            )
        return self


