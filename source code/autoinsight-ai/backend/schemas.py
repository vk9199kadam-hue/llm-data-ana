class UnifiedDataModel(BaseModel):
    """
    THE CORE DATA MODEL — Output of Stage 3/4, Input to Stages 5 & 6.
    Represents the complete enriched understanding of the dataset.

    This is passed through the entire system:
      - Stage 3: LangGraph agent produces this
      - Stage 4: Column engineering materializes derived columns
      - Stage 5: Report Engine uses this for report generation
      - Stage 6: NLQ Chat + Dashboard uses this for visualization
      - Stage 7: Frontend consumes this via REST API

    Attributes:
        original_columns: Columns present in the original CSV
        cleaned_columns: Columns after Stage 2 cleaning
        derived_columns: New columns derived by Stage 4
        relationships: Discovered column relationships
        transformation_audit: Full audit trail of all operations
        final_viz_schema: Auto-generated visualization configuration
        recommended_dashboard_layout: Suggested dashboard layout
    """
    original_columns: List[str] = Field(
        ..., min_length=1, description="Original CSV columns"
    )
    cleaned_columns: List[str] = Field(
        ..., description="Columns after Stage 2 cleaning"
    )
    derived_columns: List[DerivedColumn] = Field(
        default_factory=list, description="New columns derived by Stage 4"
    )
    relationships: List[Relationship] = Field(
        default_factory=list, description="Discovered column relationships"
    )
    transformation_audit: List[TransformationAudit] = Field(
        default_factory=list, description="Full audit trail"
    )
    final_viz_schema: Dict[str, Any] = Field(
        default_factory=dict, description="Visualization configuration"
    )
    recommended_dashboard_layout: Dict[str, Any] = Field(
        default_factory=dict, description="Suggested dashboard layout"
    )

    @property
    def total_columns(self) -> int:
        """Total columns including original + derived."""
        return len(self.cleaned_columns) + len(self.derived_columns)

    @property
    def average_relationship_confidence(self) -> float:
        """Average confidence across all discovered relationships."""
        if not self.relationships:
            return 0.0
        return sum(r.confidence for r in self.relationships) / len(self.relationships)


