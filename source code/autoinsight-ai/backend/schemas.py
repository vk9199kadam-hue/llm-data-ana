class APIResponse(BaseModel):
    """
    Standard API response envelope for all endpoints.
    Ensures consistent response format across the entire system.

    Attributes:
        status: success or error
        data: The response payload
        meta: Metadata including timestamp and request_id
        errors: List of error messages (if any)
    """
    status: str = Field(
        ..., pattern="^(success|error)$", description="Response status"
    )
    data: Optional[Dict[str, Any]] = Field(
        None, description="Response payload"
    )
    meta: Dict[str, Any] = Field(
        default_factory=lambda: {
            "timestamp": datetime.utcnow().isoformat() + "Z",
            "request_id": str(uuid4()),
            "version": "1.0.0",
        },
        description="Response metadata",
    )
    errors: List[str] = Field(
        default_factory=list, description="Error messages"
    )

    @classmethod
    def success(cls, data: Dict[str, Any], **meta_kwargs) -> "APIResponse":
        """Create a success response with the given data."""
        meta = {
            "timestamp": datetime.utcnow().isoformat() + "Z",
            "request_id": str(uuid4()),
            "version": "1.0.0",
        }
        meta.update(meta_kwargs)
        return cls(status="success", data=data, meta=meta)

    @classmethod
    def error(cls, message: str, status_code: int = 400) -> "APIResponse":
        """Create an error response with the given message."""
        return cls(
            status="error",
            data=None,
            meta={
                "timestamp": datetime.utcnow().isoformat() + "Z",
                "request_id": str(uuid4()),
                "version": "1.0.0",
                "status_code": status_code,
            },
            errors=[message],
        )


