"""
This module defines ALL Pydantic v2 data models for the AutoInsight AI system.

These models serve as the **strict data contracts** between all 8 stages:
  - Stage 1: CSV Ingestion → SchemaInferenceResponse
  - Stage 2: Data Cleaning → CleaningPlan, QualityProfile
  - Stage 3: LangGraph Agent → Relationship, DerivedColumn, UnifiedDataModel
  - Stage 4: Column Engineering → (uses UnifiedDataModel)
  - Stage 5: Report Engine → DataProfile, ReportBundle
  - Stage 6: NLQ Chat → NLQQuery, NLQResponse
  - Stage 7: Frontend → (consumes REST API)
  - Stage 8: Infrastructure → (uses all models)

Key Design Decisions:
  - All models use Pydantic v2 strict mode (validate_default=True)
  - Confidence scores are clamped to [0.0, 1.0] via Field(ge=0.0, le=1.0)
  - UUIDs are auto-generated for all database-bound models
  - Optional fields use Optional[] with None default for flexibility
"""

